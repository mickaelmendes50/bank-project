public class Person {
    private String mName;
    private String mSurname;
    private String mCPF;
    private long mPhoneNumber;

    Person(String name, String surname, String cpf, long phoneNumber) {
        mName = name;
        mSurname = surname;
        mCPF = cpf;
        mPhoneNumber = phoneNumber;
    }
}
